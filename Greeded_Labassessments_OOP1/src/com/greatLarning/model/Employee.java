package com.greatLarning.model;


public class Employee {

	  private String FirstName;
     private  String LastName;
	
	
	public Employee(String  F,String L) {
		
		this.FirstName=F;
		this.LastName=L;
	}
	
	

	public   String getFirstName() {
		return FirstName;
	}

	
	public String getLastName() {
		return LastName;
	}

	
	

}
